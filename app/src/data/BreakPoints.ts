
/**
 * Defined screen width breakpoints in pixels
 * BASED ON Bootstrap https://getbootstrap.com/docs/4.0/layout/overview/
 */
enum breakpoints {
    EXTRA_SMALL = 416,   // Smallest devices like phones, wtches etc ...
    SMALL = 576,        // Small devices such as phones, watches, etc ...  
    MEDIUM = 768,       // Medium devices such as tablets 
    LARGE = 992,        // Large devices such as laptops
    EXTRA_LARGE = 1200  // Extra large devices such as desktop, TVs 
}

export default breakpoints